# Напишіть цикл, який буде вимагати від користувача ввести слово,
# в якому є літера "h" (враховуються як великі так і маленькі).
# Цикл не повинен завершитися, якщо користувач ввів слово без букви "h".

while True:
    user_input = input("Введіть текст з літерою 'h': ")
    if 'h' in user_input or 'H' in user_input:
        print("Правильно!")
        break
    else:
        print("Неправильно. Спробуйте ще раз.")

